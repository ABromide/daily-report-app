def _mlpod_reference_sections(
    title: str,
    topic: str,
    summary: str,
    tags: list[str],
    domain: str,
    source_url: str,
    content_type: str,
) -> str:
    tag_text = "、".join(tags[:5]) or "未标注"
    return f"""## 论文或项目元信息

- **标题**：{title}
- **分类**：{topic}
- **核心标签**：{tag_text}
- **原始链接**：[{domain}]({source_url})
- **写作参考**：可以参考 MLPod 论文解读长文的节奏：先给元信息和核心图，再用长 TL;DR 概括问题、方法、结论和意义；具体 Markdown 标题和章节顺序根据原文自然组织。

如果原文、论文 PDF 或项目 README 中存在题图、系统图、结果表格、流程图或 benchmark 图，自动化建议优先保留原始图片链接。图片不能作为装饰，它应当承担证据功能：告诉读者作者把问题画成了什么结构、方法在哪一步发生作用、实验结果究竟支撑了哪一个结论。如果图片暂时无法提取，正文也建议说明已检查原文或 PDF，并记录“未能提取图片”的原因，方便下一次运行继续补齐。

![来源站点图标，仅用于样例说明](https://www.google.com/s2/favicons?domain={domain}&sz=128)

上图在样例数据里只作为来源占位；真实自动化不得满足于 favicon。真实文章应优先引用论文 figure、官方博客图片、README 架构图、实验表格截图或报告中的关键插图，并在图片下方解释它所在章节、对应证据和局限。

{_type_specific_sections(content_type, title, topic, summary, tags)}"""


def _type_specific_sections(content_type: str, title: str, topic: str, summary: str, tags: list[str]) -> str:
    if content_type == "paper":
        return _paper_sections(title, topic, summary, tags)
    if content_type == "code":
        return _code_sections(title, topic, summary, tags)
    return _blog_sections(title, topic, summary, tags)


def _paper_sections(title: str, topic: str, summary: str, tags: list[str]) -> str:
    tag_text = "、".join(tags[:5]) or "未标注"
    return f"""## 1. 引言与研究问题

论文类内容首先要回答“研究空白在哪里”。自动化不能只复述摘要，而要把作者要解决的问题放回已有方法的背景里：现有方法为什么不够，模型或数据在什么条件下失败，作者试图把问题重新定义到哪个粒度。对《{title}》来说，首页摘要“{summary}”只是入口；真正的论文解读要继续说明这个入口对应的是训练目标、评测方法、模型结构、数据分布还是部署风险。

这一节要写清楚研究问题的边界。如果论文讨论后训练，就要区分 SFT、RLHF、DPO、OPD、蒸馏、奖励建模和数据过滤各自处在训练链路的哪一段；如果论文讨论 Agent，就要区分规划、工具调用、记忆、执行环境和评测；如果论文讨论 AI 安全，就要区分能力评估、威胁模型、红队方法和治理机制。只有先把问题边界放准，后面的公式、实验和图表才不会变成堆料。

## 2. 方法与模型机制

论文解读的第二节要拆方法，不是摘方法名。需要说明输入是什么、模型如何处理、目标函数或算法步骤如何组织、输出如何被评估。看到公式时必须保留公式并解释变量。例如后训练论文常见目标可以写成：

$$
J(\\theta)=\\mathbb{{E}}_{{x,y\\sim\\pi_\\theta}}[r(x,y)]-\\beta D_{{KL}}(\\pi_\\theta \\Vert \\pi_0)
$$

公式之后需要解释：$\\pi_\\theta$ 是被优化的策略模型，$r(x,y)$ 是奖励或偏好信号，$D_{{KL}}$ 是约束新策略不要偏离参考模型的正则项。没有解释的公式只是形式感，不能帮助用户理解论文。方法部分还要说明作者为什么这样设计：是为了降低方差、稳定训练、减少标注成本、增强泛化，还是为了暴露安全风险。

## 3. 实验设计

实验设计要按 MLPod 式论文解读拆开：数据集是什么，基座模型是什么，训练设置是什么，指标如何定义，对照组和消融实验分别回答什么问题。不要只写“作者做了大量实验”。如果论文使用多个 benchmark，需要解释每个 benchmark 对应的能力维度；如果论文使用多个模型尺寸，需要说明结果是否随规模变化；如果有消融，需要说明消融是在证明哪个模块不可替代。

论文类内容还要关注失败案例。很多论文只在主表里展示平均提升，但真正有价值的部分往往在失败样本、泛化下降、成本增加或对照组异常里。自动化需要把这些内容从附录、表格或图注里挖出来，写成可以被日报追踪的问题。

## 4. 结果与图表解读

这一节必须逐图逐表解释。图表不是装饰，而是论文证据的核心载体。每张图要说明：横轴纵轴是什么，曲线或柱状图比较了什么，主结果支持哪个结论，是否存在反常点，作者有没有解释这些反常点。表格要解释每一列指标的含义，不能只说“结果提升明显”。

如果原文 PDF 中包含 figure 或 table，自动化要尽量保留原图链接或镜像到 public/assets。如果暂时无法提取图片，也必须写明已检查 PDF、HTML 页面和作者补充材料。图表解读要帮助用户理解论文，不是把图片贴上去就结束。

## 5. 讨论与相关工作

论文不能孤立看。自动化要搜索同主题论文、作者补充、Hugging Face paper page、相关博客解读、复现讨论或 GitHub 实现。如果外部材料支持原文，要说明支持的是问题设定、方法有效性还是实验结论；如果外部材料只是背景，也要标明它不能直接证明论文结论。

对于《{title}》这种归入「{topic}」的内容，相关工作部分还要解释它和标签「{tag_text}」之间的关系。标签不能替代分析，自动化必须说明它到底补上了哪一块研究拼图。

## 6. 局限性与后续追踪

论文总结要像 reviewer 一样：哪些结论已经被实验证明，哪些只在有限设置下成立，哪些需要复现，哪些可能受数据、模型规模、评测指标或实现细节影响。后续追踪要具体，比如等待代码开源、检查复现实验、比较同类方法、观察是否被下游项目采用。"""


